
public class VectorColor {
	
	int x,y,red,blue,green, RGB;

	
	public VectorColor(int X, int Y, int Red, int Blue, int Green, int rgb)
	{
		x = X;
		y = Y;
		red = Red;
		blue = Blue;
		green = Green;
		RGB =  rgb;
	}
	
	
	public void setCoordinate(int X,int Y){
		x = X;
		y = Y;
	}

}
